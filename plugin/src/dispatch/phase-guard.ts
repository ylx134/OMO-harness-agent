/**
 * Phase-Actor Authorization Engine
 *
 * Enforces the file ownership model from coordination-rules.md at the plugin level.
 * Converts the "honor system" of Expected Next Writer checks into structural gates.
 *
 * Each file in .agent-memory/ has exactly one writing actor per phase.
 * This module provides:
 *   - canActorWriteFile: check if an actor is allowed to write a file in the current phase
 *   - allowedActorsForFile: return the set of actors allowed to write a given file
 *   - guardFileWrite: returns a block reason if the write should be denied
 */

