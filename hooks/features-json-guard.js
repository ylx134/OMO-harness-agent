/**
 * Features JSON Guard Hook
 *
 * Enforces the immutability contract on features.json:
 * only the `passes` field may be changed by executing agents.
 * All other fields (id, title, description, category, priority,
 * complexity, dependencies, acceptance_criteria, verification_method,
 * verification_steps, journey_refs, state_refs) are frozen once
 * feature-planner generates the file.
 *
 * Triggered on PostToolUse for Edit/Write when targeting features.json.
 */
export default {
  name: "features-json-guard",
  description: "Prevent unauthorized modifications to features.json beyond the passes field",
  match: ["Edit", "Write"],
  handler: async ({ input, toolName }) => {
    try {
      if (!input.file_path || !input.file_path.includes('features.json')) {
        return { continue: true, suppressOutput: true };
      }

      // For Write operations: block if features.json already exists (must use Edit)
      if (toolName === 'Write') {
        try {
          await Deno.stat(input.file_path);
          // File exists — Write would overwrite the entire file, bypassing field protection
          const msg = [
            'features.json immutability violation: Write blocked on existing file.',
            'Use Edit to modify only the "passes" field.',
            'Full file overwrites are not permitted after initial creation by /feature-planner.',
            '',
            'Recovery: use Edit tool targeting only the "passes" field.'
          ].join('\n');
          console.error(JSON.stringify({
            event: "features_json_write_blocked",
            message: msg,
            file: input.file_path
          }));
          return { continue: false, output: msg };
        } catch {
          // File doesn't exist yet — allow initial creation by feature-planner
          return { continue: true, suppressOutput: true };
        }
      }

      // For Edit operations: only allow changes to "passes" field
      const oldStr = input.old_string || '';
      const newStr = input.new_string || '';

      // If both strings are empty or identical, allow
      if (!oldStr && !newStr) {
        return { continue: true, suppressOutput: true };
      }

      // Try to detect if only `passes` field changed
      // Strategy: extract all key-value changes and verify only `passes` differs
      const passesPattern = /"passes"\s*:\s*(true|false)/g;

      // Remove all `passes` fields from both strings for comparison
      const oldNormalized = oldStr.replace(passesPattern, '"passes": __PLACEHOLDER__');
      const newNormalized = newStr.replace(passesPattern, '"passes": __PLACEHOLDER__');

      if (oldNormalized !== newNormalized) {
        // Something other than `passes` was changed
        const msg = [
          'features.json immutability violation detected.',
          'Only the "passes" field may be modified by executing agents.',
          'Other fields are frozen once generated by /feature-planner.',
          'To add or modify features, use the inbox gate and run a new /feature-planner pass.',
          '',
          'Recovery: run `git checkout HEAD -- .agent-memory/features.json`'
        ].join('\n');

        console.error(JSON.stringify({
          event: "features_json_violation",
          message: msg,
          file: input.file_path
        }));

        return {
          continue: false,
          output: msg
        };
      }

      return { continue: true, suppressOutput: true };
    } catch (error) {
      // On error, allow the write but log the issue
      console.error('features-json-guard error:', error.message);
      return { continue: true, suppressOutput: true };
    }
  }
};
